// ACTIVATE: Clean up old caches and trigger background pre-cache
self.addEventListener('activate', event => {
    console.log('Activating service worker...');
    event.waitUntil(
        caches.keys().then(cacheNames => {
            return Promise.all(
                cacheNames.map(cacheName => {
                    if (cacheName !== CACHE_NAME) {
                        console.log('Deleting old cache:', cacheName);
                        return caches.delete(cacheName);
                    }
                })
            );
        }).then(() => {
            // After cleanup, trigger the background data cache.
            // This is NOT inside event.waitUntil(), so it doesn't
            // block the service worker from activating.
            console.log('Triggering background data pre-cache...');
            precacheData();
        })
    );
});

// Helper function to pre-cache all data in the background
// This is more resilient than cache.addAll()
function precacheData() {
    return caches.open(CACHE_NAME)
        .then(cache => {
            console.log('Caching data files in background...');
            
            // We loop and use .add() for each file.
            // This is NOT all-or-nothing. If one file fails,
            // the others will continue to be cached.
            DATA_URLS_TO_PRECACHE.forEach(url => {
                // We add a .catch() to each promise
                // to prevent one error from stopping the loop.
                cache.add(url).catch(error => {
                    // Log the error but don't stop the process.
                    console.warn(`Failed to pre-cache ${url}:`, error);
                });
            });
        });
}

// FETCH: Serve from cache first, then network
// This is the "Cache-First, then Network-and-Cache" strategy.
self.addEventListener('fetch', event => {
    event.respondWith(
        caches.match(event.request)
            .then(cachedResponse => {
                // 1. Return from cache if found
                if (cachedResponse) {
                    return cachedResponse;
                }

                // 2. Not in cache: fetch from network
                return fetch(event.request).then(networkResponse => {
                    // Check if we got a valid response
                    if (!networkResponse || networkResponse.status !== 200 || networkResponse.type === 'error') {
                        return networkResponse; // Just return the bad response
                    }
                    
                    // Clone the response because it's a stream
                    let responseToCache = networkResponse.clone();

                    caches.open(CACHE_NAME)
                        .then(cache => {
                            // Add the new response to the cache for next time
                            cache.put(event.request, responseToCache);
                        });

                    // Return the original response to the browser
                    return networkResponse;
                    
                }).catch(error => {
                    // This will happen if the network request fails (e.g., offline)
                    console.warn(`Fetch failed for ${event.request.url}:`, error);
                    // You could return a custom offline fallback page here if you had one
                });
            })
    );
});

